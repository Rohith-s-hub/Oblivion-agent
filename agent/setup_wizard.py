    print(_green(f"✓ Setup complete!"))
    print(_dim(f"   Config saved to: {config_env()}"))
    print(_dim(f"   Edit anytime to change settings."))
    print(_green("━" * 60))
    print()
    print(_bold("Launching Oblivion..."))
    print()
    return True


