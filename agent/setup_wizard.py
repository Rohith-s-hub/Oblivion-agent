def _color(text: str, code: str) -> str:
    """ANSI color wrapper — degrades to plain text if NO_COLOR is set."""
    if os.getenv("NO_COLOR") or not sys.stdout.isatty():
        return text
    return f"\033[{code}m{text}\033[0m"


def _green(t):   return _color(t, "1;32")
def _cyan(t):    return _color(t, "1;36")
def _yellow(t):  return _color(t, "1;33")
def _magenta(t): return _color(t, "1;35")
def _dim(t):     return _color(t, "2")
def _bold(t):    return _color(t, "1")


def _input(prompt: str, default: str = "") -> str:
    """Robust input with EOF/keyboard-interrupt handling."""
    try:
        suffix = f" [{default}]" if default else ""
        val = input(prompt + suffix + ": ").strip()
        return val or default
    except (EOFError, KeyboardInterrupt):
        print("\n\nSetup cancelled. Run `oblivion` again to retry.")
        sys.exit(1)


def _write_config(values: dict) -> None:
    """Persist config to ~/.oblivion/config.env."""
    path = config_env()
    lines = ["# Oblivion config — generated by setup wizard", ""]
    for key, val in values.items():
        if val:
            lines.append(f"{key}={val}")
    path.write_text("\n".join(lines) + "\n")


def run_wizard() -> bool:
    """Run the interactive first-time setup. Returns True if config was written."""
    print(_cyan(BANNER))
    print(_dim("                  First-time setup\n"))
    print(_bold("Welcome to Oblivion!") + " Let's get you set up in 60 seconds.")
    print()

    # ── Migration first (silent if nothing to migrate) ─────────────────────
    moved = migrate_from_legacy()
    if moved:
        print(_yellow("↻ Migrated legacy data from ~/.ai-agent/ → ~/.oblivion/:"))
        for k in moved:
            print(_dim(f"   • {k}"))
        print()
        # If we migrated a config, we're done — no wizard needed
        if "config.env" in moved:
            print(_green("✓ Your previous configuration was imported."))
            print(_dim(f"   Edit it anytime: {config_env()}"))
            print()
            return True

    # ── Step 1: Choose provider ────────────────────────────────────────────
    print(_bold("Step 1/3: Pick your LLM provider"))
    print()
    for p in PROVIDERS:
        tag = _magenta(f"[{p['tag']}]")
        print(f"  {_cyan(p['key'])}. {_bold(p['name']):28s} {tag}")
        print(f"     {_dim(p['details'])}")
    print()
    choice = _input("Choose 1-4", "1")

    provider = next((p for p in PROVIDERS if p["key"] == choice), PROVIDERS[0])
    print()
    print(_green(f"✓ Selected: {provider['name']}"))
    print()

    # ── Step 2: API key (if needed) ────────────────────────────────────────
    config = {
        "DEFAULT_MODEL": provider["default_model"],
        "WORKSPACE_DIR": str(Path.cwd()),
        "MAX_ITERATIONS": "40",
        "MAX_TOKENS": "1500",
        "TEMPERATURE": "0.05",
        "REQUIRE_APPROVAL_FOR_WRITE": "true",
        "REQUIRE_APPROVAL_FOR_BASH": "true",
        "FRIDAY_ENABLED": "true",
        "FRIDAY_NAME": "boss",
        "FRIDAY_VOICE": "aria",
        "OBLIVION_PRELOAD_VOICE": "1",
        "PARALLEL_EMBEDDINGS": "8",
        "EMBED_MODEL": "all-minilm",
        "LITELLM_LOCAL_MODEL_COST_MAP": "True",
    }

    if provider["env_key"]:
        print(_bold(f"Step 2/3: Paste your {provider['name']} API key"))
        print(_dim(f"   Get one free: {provider['signup_url']}"))
        print()
